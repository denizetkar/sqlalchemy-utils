"""
Alembic migration operations for database views.

Provides 7 MigrateOperation subclasses for creating, dropping, replacing,
and refreshing regular and materialized views, along with ``op.*`` helper
functions.

Usage in Alembic migrations::

    def upgrade():
        op.create_view("my_view", "SELECT id, name FROM users")
        op.create_materialized_view("mv_stats", "SELECT count(*) FROM events")

    def downgrade():
        op.drop_view("my_view")
        op.drop_materialized_view("mv_stats")
"""

from __future__ import annotations

import warnings

import sqlalchemy as sa
from alembic.operations import MigrateOperation, Operations

from sqlalchemy_utils.view import _quote_qualified_name


def _validate_definition(definition: str) -> None:
    if not isinstance(definition, str) or not definition:
        raise TypeError(
            f"definition must be a non-empty string, got {type(definition).__name__}"
        )


def _validate_name(name: str) -> None:
    if not isinstance(name, str) or not name:
        raise TypeError(
            f"name must be a non-empty string, got {type(name).__name__}"
        )


# ===================================================================
# Regular view operations
# ===================================================================


@Operations.register_operation("create_view")
class CreateViewOp(MigrateOperation):
    """Operation that emits ``CREATE [OR REPLACE] VIEW``.

    :param name: Name of the view to create.
    :param definition: SQL ``SELECT`` statement defining the view body.
    :param schema: Optional schema name; when given, the view name is
        schema-qualified in the emitted DDL.
    :param replace: Deprecated. When ``True``, emits ``CREATE OR REPLACE
        VIEW``; prefer ``op.replace_view()`` / :class:`ReplaceViewOp`
        instead, whose ``reverse()`` restores the prior definition.
    :param cascade_on_drop: When ``True`` (default), the autogenerated
        downgrade (``DROP VIEW``) uses ``CASCADE`` so dependent objects
        are removed as well.
    """

    def __init__(
        self,
        name: str,
        definition: str,
        *,
        schema: str | None = None,
        replace: bool = False,
        cascade_on_drop: bool = True,
    ) -> None:
        _validate_name(name)
        _validate_definition(definition)
        if replace:
            warnings.warn(
                "CreateViewOp(replace=True) is deprecated; use op.replace_view() "
                "or ReplaceViewOp instead. The reverse() of CreateViewOp(replace=True) "
                "emits a destructive DROP, while ReplaceViewOp.reverse() restores "
                "the prior definition.",
                DeprecationWarning,
                stacklevel=2,
            )
        self.name = name
        self.definition = definition
        self.schema = schema or None
        self.replace = replace
        self.cascade_on_drop = cascade_on_drop

    @classmethod
    def create_view(
        cls,
        operations: Operations,
        name: str,
        definition: str,
        *,
        schema: str | None = None,
        replace: bool = False,
        cascade_on_drop: bool = True,
    ) -> None:
        """Entry point for ``op.create_view()``. See class docstring for parameter details.

        Example::

            op.create_view("my_view", "SELECT id, name FROM users")
        """
        op = CreateViewOp(
            name, definition, schema=schema, replace=replace,
            cascade_on_drop=cascade_on_drop,
        )
        return operations.invoke(op)

    def reverse(self) -> DropViewOp:
        """Return the inverse: drop the view that was just created."""
        return DropViewOp(
            self.name,
            schema=self.schema,
            cascade=self.cascade_on_drop,
            definition=self.definition,
        )


@Operations.register_operation("drop_view")
class DropViewOp(MigrateOperation):
    """Operation that emits ``DROP VIEW IF EXISTS``.

    :param name: Name of the view to drop.
    :param schema: Optional schema name; when given, the view name is
        schema-qualified in the emitted DDL.
    :param cascade: When ``True`` (default), appends ``CASCADE`` to the
        ``DROP VIEW``. This corresponds to ``cascade_on_drop`` in the
        runtime DDL API (:func:`~sqlalchemy_utils.view.create_view`).
    :param definition: Optional stored definition of the view. When
        supplied, :meth:`reverse` can re-create the view for an
        autogenerated downgrade; otherwise :meth:`reverse` raises
        ``NotImplementedError``.
    """

    def __init__(
        self,
        name: str,
        *,
        schema: str | None = None,
        # Named ``cascade`` for Alembic op convention; corresponds to
        # ``cascade_on_drop`` in create_view() and ViewMixin.__view_cascade__.
        cascade: bool = True,
        definition: str | None = None,
    ) -> None:
        _validate_name(name)
        if definition is not None:
            _validate_definition(definition)
        self.name = name
        self.schema = schema or None
        self.cascade = cascade
        self.definition = definition

    @classmethod
    def drop_view(
        cls,
        operations: Operations,
        name: str,
        *,
        schema: str | None = None,
        cascade: bool = True,
        definition: str | None = None,
    ) -> None:
        """Entry point for ``op.drop_view()``. See class docstring for parameter details.

        Example::

            op.drop_view("my_view")
        """
        op = DropViewOp(
            name,
            schema=schema,
            cascade=cascade,
            definition=definition,
        )
        return operations.invoke(op)

    def reverse(self) -> CreateViewOp:
        """Return the inverse: re-create the view that was just dropped.

        Requires ``definition`` to have been stored at construction time;
        otherwise a ``NotImplementedError`` is raised because the view definition
        is unknown.
        """
        if self.definition is None:
            raise NotImplementedError(
                f"Cannot reverse DropViewOp for '{self.name}': "
                "no definition stored. Pass definition= to DropViewOp "
                "to enable automatic downgrade generation."
            )
        return CreateViewOp(
            self.name, self.definition, schema=self.schema,
            cascade_on_drop=self.cascade,
        )

@Operations.register_operation("replace_view")
class ReplaceViewOp(MigrateOperation):
    """Operation that emits ``CREATE OR REPLACE VIEW``.

    :param name: Name of the view to replace.
    :param definition: New SQL ``SELECT`` statement defining the view body.
    :param schema: Optional schema name; when given, the view name is
        schema-qualified in the emitted DDL.
    :param old_definition: Previous definition of the view. When
        supplied, :meth:`reverse` can restore the prior definition for an
        autogenerated downgrade; otherwise :meth:`reverse` raises
        ``NotImplementedError``.
    """

    def __init__(
        self,
        name: str,
        definition: str,
        *,
        schema: str | None = None,
        old_definition: str | None = None,
    ) -> None:
        _validate_name(name)
        _validate_definition(definition)
        self.name = name
        self.definition = definition
        self.schema = schema or None
        self.old_definition = old_definition

    @classmethod
    def replace_view(
        cls,
        operations: Operations,
        name: str,
        definition: str,
        *,
        schema: str | None = None,
        old_definition: str | None = None,
    ) -> None:
        """Entry point for ``op.replace_view()``. See class docstring for parameter details.

        Example::

            op.replace_view(
                "my_view",
                "SELECT id, name, email FROM users",
                old_definition="SELECT id, name FROM users",
            )
        """
        op = ReplaceViewOp(
            name, definition, schema=schema, old_definition=old_definition
        )
        return operations.invoke(op)

    def reverse(self) -> ReplaceViewOp:
        """Return the inverse: replace back to the old definition.

        Requires ``old_definition``; otherwise raises ``NotImplementedError``.
        """
        if self.old_definition is None:
            raise NotImplementedError(
                f"Cannot reverse ReplaceViewOp for '{self.name}': "
                "no old_definition stored. Pass old_definition= to "
                "ReplaceViewOp to enable automatic downgrade generation."
            )
        return ReplaceViewOp(
            self.name, self.old_definition, schema=self.schema,
            old_definition=self.definition,
        )


