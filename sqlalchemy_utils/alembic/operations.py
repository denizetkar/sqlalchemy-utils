@Operations.register_operation("create_materialized_view")
class CreateMaterializedViewOp(MigrateOperation):
    """Operation that emits ``CREATE MATERIALIZED VIEW``.

    :param name: Name of the materialized view to create.
    :param definition: SQL ``SELECT`` statement defining the materialized
        view body.
    :param schema: Optional schema name; when given, the view name is
        schema-qualified in the emitted DDL.
    :param with_data: When ``True``, emits ``WITH DATA`` (populates the
        materialized view immediately). When ``False`` (default), emits
        ``WITH NO DATA`` (creates the MV empty).
    :param cascade_on_drop: When ``True`` (default), the autogenerated
        downgrade (``DROP MATERIALIZED VIEW``) uses ``CASCADE`` so
        dependent objects are removed as well.

    .. note:: Materialized views are PostgreSQL-specific; other dialects
       will raise at execute time.

    .. note::
       Autogenerate always emits ``with_data=False`` (unpopulated MVs);
       manual ``op.create_materialized_view()`` also defaults to
       ``WITH NO DATA`` for consistency.
    """

    def __init__(
        self,
        name: str,
        definition: str,
        *,
        schema: str | None = None,
        with_data: bool = False,
        cascade_on_drop: bool = True,
    ) -> None:
        _validate_name(name)
        _validate_definition(definition)
        self.name = name
        self.definition = definition
        self.schema = schema or None
        self.with_data = with_data
        self.cascade_on_drop = cascade_on_drop

    @classmethod
    def create_materialized_view(
        cls,
        operations: Operations,
        name: str,
        definition: str,
        *,
        schema: str | None = None,
        with_data: bool = False,
        cascade_on_drop: bool = True,
    ) -> None:
        """Entry point for ``op.create_materialized_view()``. See class docstring for parameter details.

        Example::

            op.create_materialized_view(
                "mv_stats", "SELECT count(*) FROM events"
            )
        """
        op = CreateMaterializedViewOp(
            name, definition, schema=schema, with_data=with_data,
            cascade_on_drop=cascade_on_drop,
        )
        return operations.invoke(op)

    def reverse(self) -> DropMaterializedViewOp:
        """Return the inverse: drop the materialized view just created."""
        return DropMaterializedViewOp(
            self.name,
            schema=self.schema,
            cascade=self.cascade_on_drop,
            definition=self.definition,
            with_data=self.with_data,
        )

@Operations.register_operation("drop_materialized_view")
class DropMaterializedViewOp(MigrateOperation):
    """Operation that emits ``DROP MATERIALIZED VIEW IF EXISTS``.

    :param name: Name of the materialized view to drop.
    :param schema: Optional schema name; when given, the view name is
        schema-qualified in the emitted DDL.
    :param cascade: When ``True`` (default), appends ``CASCADE`` to the
        ``DROP MATERIALIZED VIEW``. This corresponds to
        ``cascade_on_drop`` in the runtime DDL API
        (:func:`~sqlalchemy_utils.view.create_materialized_view`).
    :param definition: Optional stored definition of the materialized
        view. When supplied, :meth:`reverse` can re-create the MV for an
        autogenerated downgrade; otherwise :meth:`reverse` raises
        ``NotImplementedError``.
    :param with_data: Carried solely so :meth:`reverse` can restore the
        materialized view with the original ``WITH DATA`` / ``WITH NO
        DATA`` clause; the ``DROP`` itself ignores it.

    .. note:: Materialized views are PostgreSQL-specific; other dialects
       will raise at execute time.
    """

    def __init__(
        self,
        name: str,
        *,
        schema: str | None = None,
        # Named ``cascade`` for Alembic op convention; corresponds to
        # ``cascade_on_drop`` in create_view() and ViewMixin.__view_cascade__.
        cascade: bool = True,
        definition: str | None = None,
        with_data: bool = False,
    ) -> None:
        _validate_name(name)
        if definition is not None:
            _validate_definition(definition)
        self.name = name
        self.schema = schema or None
        self.cascade = cascade
        self.definition = definition
        self.with_data = with_data

    @classmethod
    def drop_materialized_view(
        cls,
        operations: Operations,
        name: str,
        *,
        schema: str | None = None,
        cascade: bool = True,
        definition: str | None = None,
        with_data: bool = False,
    ) -> None:
        """Entry point for ``op.drop_materialized_view()``. See class docstring for parameter details.

        Example::

            op.drop_materialized_view("mv_stats")
        """
        op = DropMaterializedViewOp(
            name, schema=schema, cascade=cascade, definition=definition,
            with_data=with_data,
        )
        return operations.invoke(op)

    def reverse(self) -> CreateMaterializedViewOp:
        """Return the inverse: re-create the materialized view.

        Requires ``definition``; otherwise raises ``NotImplementedError``.
        """
        if self.definition is None:
            raise NotImplementedError(
                f"Cannot reverse DropMaterializedViewOp for '{self.name}': "
                "no definition stored. Pass definition= to "
                "DropMaterializedViewOp to enable automatic downgrade "
                "generation."
            )
        return CreateMaterializedViewOp(
            self.name, self.definition, schema=self.schema,
            with_data=self.with_data,
            cascade_on_drop=self.cascade,
        )

@Operations.register_operation("replace_materialized_view")
class ReplaceMaterializedViewOp(MigrateOperation):
    """Operation that drops and re-creates a materialized view.

    PostgreSQL does not support ``CREATE OR REPLACE MATERIALIZED VIEW``
    so this operation issues a ``DROP`` followed by a ``CREATE``.

    :param name: Name of the materialized view to replace.
    :param definition: New SQL ``SELECT`` statement defining the
        materialized view body.
    :param schema: Optional schema name; when given, the view name is
        schema-qualified in the emitted DDL.
    :param with_data: When ``True``, emits ``WITH DATA`` (populates the
        materialized view immediately). When ``False`` (default), emits
        ``WITH NO DATA`` (creates the MV empty).
    :param cascade: When ``True`` (default), appends ``CASCADE`` to the
        ``DROP MATERIALIZED VIEW``.
    :param old_definition: Previous definition of the materialized view.
        When supplied, :meth:`reverse` can restore the prior definition
        for an autogenerated downgrade; otherwise :meth:`reverse` raises
        ``NotImplementedError``.

    .. note:: Materialized views are PostgreSQL-specific; other dialects
       will raise at execute time.

    .. note::
       Autogenerate always emits ``with_data=False`` (unpopulated MVs);
       manual ``op.replace_materialized_view()`` also defaults to
       ``WITH NO DATA`` for consistency.
    """

    def __init__(
        self,
        name: str,
        definition: str,
        *,
        schema: str | None = None,
        with_data: bool = False,
        cascade: bool = True,
        old_definition: str | None = None,
    ) -> None:
        _validate_name(name)
        _validate_definition(definition)
        self.name = name
        self.definition = definition
        self.schema = schema or None
        self.with_data = with_data
        self.cascade = cascade
        self.old_definition = old_definition
    @classmethod
    def replace_materialized_view(
        cls,
        operations: Operations,
        name: str,
        definition: str,
        *,
        schema: str | None = None,
        with_data: bool = False,
        cascade: bool = True,
        old_definition: str | None = None,
    ) -> None:
        """Entry point for ``op.replace_materialized_view()``. See class docstring for parameter details.

        Example::

            op.replace_materialized_view(
                "mv_stats",
                "SELECT count(*), max(ts) FROM events",
                old_definition="SELECT count(*) FROM events",
            )
        """
        op = ReplaceMaterializedViewOp(
            name,
            definition,
            schema=schema,
            with_data=with_data,
            cascade=cascade,
            old_definition=old_definition,
        )
        return operations.invoke(op)

    def reverse(self) -> ReplaceMaterializedViewOp:
        """Return the inverse: replace back to old definition.

        Requires ``old_definition``; otherwise raises ``NotImplementedError``.
        """
        if self.old_definition is None:
            raise NotImplementedError(
                f"Cannot reverse ReplaceMaterializedViewOp for "
                f"'{self.name}': no old_definition stored. Pass "
                "old_definition= to ReplaceMaterializedViewOp to enable "
                "automatic downgrade generation."
            )
        return ReplaceMaterializedViewOp(
            self.name,
            self.old_definition,
            schema=self.schema,
            with_data=self.with_data,
            cascade=self.cascade,
            old_definition=self.definition,
        )

